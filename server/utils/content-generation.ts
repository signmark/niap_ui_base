/**
 * Генерирует случайную подпись для Instagram Stories
 * @returns Случайная подпись для Instagram Stories
 */
export function generateRandomCaption(): string {
  const captions = [
    'Смотрите наши новости!',
    'Новая публикация для вас!',
    'Специально для наших подписчиков!',
    'Не пропустите это!',
    'Эксклюзивный контент!',
    'Смотрите в нашей истории',
    'Свежие новости от нас',
  ];
  
  return captions[Math.floor(Math.random() * captions.length)];
}